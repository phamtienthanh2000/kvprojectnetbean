    void loadDataTable(ArrayList<Bill> bills) {
        String[][] data = new String[bills.size()+1][9];
        int sum = 0;
        for (int i = 0; i <= bills.size(); i++) {
           
            if (i == bills.size()) {
                data[i][0] = "Tổng tiền hàng";
                data[i][4] = sum+"";
                break;
            }
            Bill bill = bills.get(i);
            OrderLine orderLine = bill.getOrderLines().get(0);
            data[i][0] = i+1+"";
            data[i][1] = bill.getWriteDate().format(DateTimeFormatter.ofPattern("d-M-y"));
            data[i][2] = orderLine.getAmount()+"";
            data[i][3] = orderLine.getSellPrice()+"";
            data[i][4] = orderLine.getTotal()+"";
            data[i][5] = bill.getId()+"";
            data[i][6] = bill.getClient().getName();
            data[i][7] = bill.getClient().getId()+"";
            data[i][8] = bill.getUser().getFullName();
            sum += orderLine.getTotal();
        }

        DefaultTableModel model = new DefaultTableModel(data, new String [] {
                "No .", "Ngày bán", "Số lượng bán", "Giá bán", "Tổng tiền", "Mã hóa đơn", "Tên khách hàng", "Mã khách hàng", "Người bán"
            } );
        tblStatDetail.setModel(model);
    }
