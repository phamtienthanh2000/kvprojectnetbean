    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String startDate = txtStartDate.getText();
        String endDate = txtEndDate.getText();

        String regexDate = "^\\d{2}-\\d{2}-\\d{4}$";
        if(!Pattern.matches(regexDate, startDate)){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập ngày bắt đầu vd : 18-12-1972");
        return;
        }
        if (!Pattern.matches(regexDate, endDate)) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập ngày kết thúc ex : 25-10-1965");
                    return;
        }
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("d-M-y");
        System.out.println(LocalDate.parse(startDate, formatter));
        System.out.println(LocalDate.parse(endDate, formatter));

        ArrayList<Bill> searchBills = billDAO.getBillsByProductIdAndDateBetweenStartDateAndEndDate(stat.getId(), LocalDate.parse(startDate, formatter), LocalDate.parse(endDate, formatter));
        loadDataTable(searchBills);

    }//GEN-LAST:event_btnSearchActionPerformed

